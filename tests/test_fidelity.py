class TestMarkdownReport:
    def test_returns_string(self, fidelity_report):
        md = fidelity_report.to_markdown()
        assert isinstance(md, str)
        assert len(md) > 100

    def test_contains_section_headers(self, fidelity_report):
        md = fidelity_report.to_markdown()
        assert "Marginal Fidelity" in md
        assert "Correlation Fidelity" in md
        assert "Tail Risk" in md

    def test_contains_column_names(self, fidelity_report, medium_motor_df):
        md = fidelity_report.to_markdown()
        for col in ["driver_age", "exposure", "claim_count"]:
            assert col in md

    def test_markdown_table_formatting(self, fidelity_report):
        md = fidelity_report.to_markdown()
        # Should contain pipe-delimited table rows
        lines = [l for l in md.split("\n") if "|" in l]
        assert len(lines) > 5


