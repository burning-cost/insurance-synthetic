class TestMissingColumnWarning:
    def test_warns_on_missing_synthetic_columns(self, medium_motor_df):
        reduced = medium_motor_df.drop("claim_amount")
        with pytest.warns(UserWarning, match="missing columns"):
            report = SyntheticFidelityReport(medium_motor_df, reduced, target_col="claim_count")
        # marginal report should still work for shared columns
        marg = report.marginal_report()
        assert isinstance(marg, pl.DataFrame)
