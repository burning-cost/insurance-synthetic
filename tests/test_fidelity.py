# Correlation report
# ---------------------------------------------------------------------------

class TestCorrelationReport:
    def test_returns_dataframe(self, fidelity_report):
        result = fidelity_report.correlation_report()
        assert isinstance(result, pl.DataFrame)

    def test_has_expected_columns(self, fidelity_report):
        result = fidelity_report.correlation_report()
        required = {"col_a", "col_b", "spearman_real", "spearman_synthetic",
                    "delta", "frobenius_norm"}
        assert required.issubset(set(result.columns))

    def test_correlations_in_minus_one_to_one(self, fidelity_report):
        result = fidelity_report.correlation_report()
        r = result["spearman_real"].to_numpy()
        s = result["spearman_synthetic"].to_numpy()
        assert (r >= -1.0).all() and (r <= 1.0).all()
        assert (s >= -1.0).all() and (s <= 1.0).all()

    def test_delta_non_negative(self, fidelity_report):
        result = fidelity_report.correlation_report()
        assert (result["delta"].to_numpy() >= 0).all()

    def test_sorted_by_delta_descending(self, fidelity_report):
        result = fidelity_report.correlation_report()
        deltas = result["delta"].to_numpy()
        assert all(deltas[i] >= deltas[i + 1] for i in range(len(deltas) - 1))

    def test_frobenius_norm_positive(self, fidelity_report):
        result = fidelity_report.correlation_report()
        frob = float(result["frobenius_norm"][0])
        assert frob >= 0.0


# ---------------------------------------------------------------------------
# TVaR ratio
# ---------------------------------------------------------------------------

class TestTVaRRatio:
    def test_returns_float(self, fidelity_report):
        ratio = fidelity_report.tvar_ratio("claim_count")
        assert isinstance(ratio, float)

    def test_tvar_ratio_positive(self, fidelity_report):
        ratio = fidelity_report.tvar_ratio("claim_count")
        assert ratio >= 0.0

    def test_tvar_ratio_at_different_percentiles(self, fidelity_report):
        """TVaR ratio should be computable at various percentiles."""
        for p in [0.90, 0.95, 0.99]:
            r = fidelity_report.tvar_ratio("claim_count", percentile=p)
            assert np.isfinite(r)

    def test_tvar_ratio_of_identical_data(self):
        """TVaR ratio of identical data should be 1.0."""
        rng = np.random.default_rng(42)
        data = rng.lognormal(5, 1.2, size=500)
        df = pl.DataFrame({"x": data.tolist(), "exposure": [1.0] * 500})
        report = SyntheticFidelityReport(df, df, target_col="x")
        ratio = report.tvar_ratio("x")
        assert ratio == pytest.approx(1.0, abs=1e-6)


# ---------------------------------------------------------------------------
# Exposure-weighted KS
# ---------------------------------------------------------------------------

class TestExposureWeightedKS:
    def test_returns_float(self, fidelity_report):
        result = fidelity_report.exposure_weighted_ks("driver_age")
        assert isinstance(result, float)

    def test_in_zero_one(self, fidelity_report):
        result = fidelity_report.exposure_weighted_ks("driver_age")
        assert 0.0 <= result <= 1.0

    def test_missing_exposure_warns(self, real_synthetic_pair):
        real_df, synthetic_df = real_synthetic_pair
        report = SyntheticFidelityReport(
            real_df.drop("exposure"),
            synthetic_df.drop("exposure"),
            exposure_col="exposure",
            target_col="claim_count",
        )
        with pytest.warns(UserWarning, match="Exposure"):
            result = report.exposure_weighted_ks("driver_age")
        assert isinstance(result, float)


