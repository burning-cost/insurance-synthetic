"""
Tests for SyntheticFidelityReport.

We use a simple real/synthetic pair where the synthetic is generated by the
InsuranceSynthesizer so we're testing the full pipeline end-to-end, then
checking that the report metrics are structurally correct (right shapes,
right types, valid ranges).

We do not test CatBoost (TSTR) here — it's in optional dependencies and would
make the test suite much slower. There's a separate slow test marker for that.
"""

import numpy as np
import polars as pl
import pytest

from insurance_synthetic import InsuranceSynthesizer, SyntheticFidelityReport


# ---------------------------------------------------------------------------
# Shared fixture: real/synthetic pair
# ---------------------------------------------------------------------------

@pytest.fixture(scope="module")
def real_synthetic_pair(medium_motor_df):
    synth = InsuranceSynthesizer(random_state=42)
    synth.fit(
        medium_motor_df,
        exposure_col="exposure",
        frequency_col="claim_count",
        severity_col="claim_amount",
    )
    synthetic_df = synth.generate(1000)
    return medium_motor_df, synthetic_df


@pytest.fixture(scope="module")
def fidelity_report(real_synthetic_pair):
    real_df, synthetic_df = real_synthetic_pair
    return SyntheticFidelityReport(
        real_df, synthetic_df, exposure_col="exposure", target_col="claim_count"
    )


# ---------------------------------------------------------------------------
# Marginal report
# ---------------------------------------------------------------------------

class TestMarginalReport:
    def test_returns_dataframe(self, fidelity_report):
        result = fidelity_report.marginal_report()
        assert isinstance(result, pl.DataFrame)

    def test_has_expected_columns(self, fidelity_report):
        result = fidelity_report.marginal_report()
        required = {"column", "ks_statistic", "ks_pvalue", "wasserstein",
                    "mean_real", "mean_synthetic", "std_real", "std_synthetic"}
        assert required.issubset(set(result.columns))

    def test_row_count_matches_columns(self, fidelity_report, medium_motor_df):
        result = fidelity_report.marginal_report()
        assert len(result) == len(medium_motor_df.columns)

    def test_ks_statistic_in_zero_one(self, fidelity_report):
        result = fidelity_report.marginal_report()
        numeric_rows = result.filter(pl.col("ks_statistic").is_not_null())
        ks = numeric_rows["ks_statistic"].to_numpy()
        assert (ks >= 0).all()
        assert (ks <= 1).all()

    def test_wasserstein_non_negative(self, fidelity_report):
        result = fidelity_report.marginal_report()
        numeric_rows = result.filter(pl.col("wasserstein").is_not_null())
        wass = numeric_rows["wasserstein"].to_numpy()
        assert (wass >= 0).all()

    def test_good_synthesis_ks_below_threshold(self, fidelity_report):
        """
        For a well-fitted synthesiser on 1000 rows with 1000 synthetic,
        most columns should have KS stat < 0.15 (a loose threshold for small n).
        """
        result = fidelity_report.marginal_report()
        numeric_rows = result.filter(pl.col("ks_statistic").is_not_null())
        median_ks = float(numeric_rows["ks_statistic"].median())
        assert median_ks < 0.2, f"Median KS {median_ks:.3f} is too high"


